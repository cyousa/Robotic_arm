void Set_DAC_Value(short PWM_A,short PWM_B)
{
	if(PWM_A>=0)
	{
		  HAL_GPIO_WritePin(GPIOA, Motor_IN1_Pin, 1);
			HAL_GPIO_WritePin(GPIOA, Motor_IN2_Pin,0);
			HAL_DAC_SetValue(&hdac1,DAC_CHANNEL_1,DAC_ALIGN_12B_R,PWM_A);
	}
	else if(PWM_A<0)
	{
			HAL_GPIO_WritePin(GPIOA, Motor_IN1_Pin, 0);
			HAL_GPIO_WritePin(GPIOA, Motor_IN2_Pin,1);
			HAL_DAC_SetValue(&hdac1,DAC_CHANNEL_1,DAC_ALIGN_12B_R,(-PWM_A));
	}
		if(PWM_B>=0)
	{
		  HAL_GPIO_WritePin(GPIOC, Motor_IN3_Pin, 1);
			HAL_GPIO_WritePin(GPIOB, Motor_IN4_Pin, 0);
			HAL_DAC_SetValue(&hdac1,DAC_CHANNEL_2,DAC_ALIGN_12B_R,(PWM_B));

	} 
	else if(PWM_B<0)
	{
		  HAL_GPIO_WritePin(GPIOC, Motor_IN3_Pin, 0);
			HAL_GPIO_WritePin(GPIOB, Motor_IN4_Pin, 1);
			HAL_DAC_SetValue(&hdac1,DAC_CHANNEL_2,DAC_ALIGN_12B_R,(-PWM_B));
	}

	
}

